package musicregister;

public class Song {

    private int keyNumber;
    private String adress;
    private String owner;
    
    public Song() {
        
    }
    
    public Song(int keyNumber, String adress, String owner) {
        
        this.keyNumber = keyNumber;
        this.adress = adress;
        this.owner = owner;
        
    }
    
}
